package com.spring.autowire;

public class Speaker {
	private String type;
	private int volLevel;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getVolLevel() {
		return volLevel;
	}
	public void setVolLevel(int volLevel) {
		this.volLevel = volLevel;
	}


}
